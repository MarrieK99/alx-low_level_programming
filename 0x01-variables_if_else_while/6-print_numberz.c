#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: This program prints all single-digit numbers of base 10
 * starting from 0, followed by a new line.
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	/* Print the digits using putchar */
	putchar('0');
	putchar('1');
	putchar('2');
	putchar('3');
	putchar('4');
	putchar('5');
	putchar('6');
	putchar('7');
	putchar('8');
	putchar('9');
	putchar('\n');

	return (0);
}
